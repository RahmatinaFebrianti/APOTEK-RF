     private void penomoran_otomatis() {
        int no_karyawan=0;
        String temp_karyawan="";
        try {
            koneksi();
            String sql = "select id_karyawan from tb_karyawan order by id_karyawan desc limit 1";                        
            ResultSet res=stt.executeQuery(sql);
            while (res.next()) {
                temp_karyawan = res.getString(1);                
                String nomor_blkg = temp_karyawan.substring(2,4);
                no_karyawan=Integer.parseInt(nomor_blkg);
            }
                        
            no_karyawan++;
            String nilai=String.valueOf(no_karyawan);                      
            if(nilai.length()==1){
                eid.setText("K00"+no_karyawan);
            }else
            if(nilai.length()==2){
                eid.setText("K0"+no_karyawan);
            }else
            if(nilai.length()==3){
                eid.setText("K"+no_karyawan);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Penomoran !");
        }
    }

